    <h2>Job Position 1</h2>
    <h3>Reference Number: WWD01</h3>

    <p><strong>Job Title:</strong> Web Developer</p>

    <p><strong>Description:</strong>
    The Web Developer is responsible for designing, developing, and maintaining Website Wellness’s online patient platforms and health information websites. The role ensures systems are secure, responsive, accessible, and easy for patients and healthcare professionals to use.
    </p>

    <p><strong>Salary:</strong> $70,000 – $90,000</p>
    <p><strong>Reports to:</strong> Digital Services Manager</p>

    <h4>Key Responsibilities</h4>
    <ul>
    <li>Develop and maintain company websites and patient portals</li>
    <li>Implement responsive and accessible web designs</li>
    <li>Integrate appointment booking and healthcare management systems</li>
    <li>Perform website testing, debugging, and performance optimisation</li>
    <li>Maintain website security and data protection standards</li>
    </ul>

    <h4>Essential Requirements</h4>
    <ol>
    <li>Experience with HTML, CSS, JavaScript, and modern web frameworks</li>
    <li>Understanding of responsive web design principles</li>
    <li>Knowledge of web security and privacy practices</li>
    <li>Ability to work collaboratively within a development team</li>
    </ol>

    <h4>Preferable Requirements</h4>
    <ul>
    <li>Experience working with healthcare or booking platforms</li>
    <li>Knowledge of UX/UI design principles</li>
    </ul>

