    <h2>Job Position 2</h2>
    <h3>Reference Number: WWD02</h3>

    <p><strong>Job Title:</strong> Digital Health Support Specialist</p>

    <p><strong>Description:</strong>
    The Digital Health Support Specialist assists patients and staff in using Website Wellness digital services, including appointment systems, online health platforms, and telehealth tools. The role focuses on improving user experience and providing technical support.
    </p>

    <p><strong>Salary:</strong> $60,000 – $75,000</p>
    <p><strong>Reports to:</strong> Operations Manager</p>

    <h4>Key Responsibilities</h4>
    <ul>
    <li>Provide technical support for online patient systems</li>
    <li>Assist users with account setup and troubleshooting</li>
    <li>Monitor digital platform performance and report issues</li>
    <li>Support telehealth and online appointment services</li>
    <li>Create user guides and support documentation</li>
    </ul>

    <h4>Essential Requirements</h4>
    <ol>
    <li>Strong communication and customer support skills</li>
    <li>Basic understanding of web-based systems and digital platforms</li>
    <li>Problem-solving and troubleshooting abilities</li>
    <li>Ability to explain technical concepts clearly to users</li>
    </ol>

    <h4>Preferable Requirements</h4>
    <ul>
    <li>Experience in healthcare administration or IT support</li>
    <li>Familiarity with telehealth or patient management systems</li>
